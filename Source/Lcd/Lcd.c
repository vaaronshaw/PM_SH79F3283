void init_lcd()
{
	/*****************************************************************************************************
	1.选择1/4占空比，1/3偏置 ,COM5~8共享为SEG25~28
	2.偏置电阻900K
	******************************************************************************************************/
	DISPCON1 = 0;
	DISPCLK0 = 0;	//64Hz

	/******************************************************************************************************
	配置COM口、SEG口(全部为LCD口)
	注：用户可根据需要配置相应数目的COM口和SEG口	
	********************************************************************************************************/
	LCDSEG0 = 0xff;
	LCDSEG1 = 0xff;
	LCDSEG2 = 0xff;
	LEDCOM = 0xff;
	/******************************************************************************************************
	对比度为Vdd.	
	********************************************************************************************************/
	LCDCON  = 0x8F;
}
